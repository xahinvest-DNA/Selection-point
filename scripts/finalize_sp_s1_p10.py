from pathlib import Path
import re

ROOT = Path('.')


def read(path: str) -> str:
    return (ROOT / path).read_text(encoding='utf-8')


def write(path: str, text: str) -> None:
    target = ROOT / path
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(text, encoding='utf-8')


def replace_once(path: str, old: str, new: str) -> None:
    text = read(path)
    if old not in text:
        raise RuntimeError(f'Anchor not found in {path}: {old[:180]!r}')
    write(path, text.replace(old, new, 1))


def replace_all(path: str, old: str, new: str) -> None:
    text = read(path)
    if old in text:
        write(path, text.replace(old, new))


def regex_once(path: str, pattern: str, replacement: str, flags=re.MULTILINE | re.DOTALL) -> None:
    text = read(path)
    updated, count = re.subn(pattern, lambda _: replacement, text, count=1, flags=flags)
    if count != 1:
        raise RuntimeError(f'Regex matched {count} times in {path}: {pattern[:180]!r}')
    write(path, updated)


def replace_nth(path: str, old: str, new: str, occurrence: int) -> None:
    text = read(path)
    start = -1
    cursor = 0
    for _ in range(occurrence):
        start = text.find(old, cursor)
        if start < 0:
            raise RuntimeError(f'Occurrence {occurrence} not found in {path}: {old!r}')
        cursor = start + len(old)
    write(path, text[:start] + new + text[start + len(old):])


