from __future__ import annotations

import re
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]


def path(rel: str) -> Path:
    return ROOT / rel


def read(rel: str) -> str:
    return path(rel).read_text(encoding="utf-8")


def write(rel: str, content: str) -> None:
    p = path(rel)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(content.rstrip() + "\n", encoding="utf-8")


def replace_exact(rel: str, old: str, new: str, expected: int = 1) -> None:
    text = read(rel)
    actual = text.count(old)
    if actual != expected:
        raise RuntimeError(f"{rel}: expected {expected} occurrences, found {actual}: {old!r}")
    write(rel, text.replace(old, new))


def replace_regex(rel: str, pattern: str, new: str, expected: int = 1, flags: int = re.S) -> None:
    text = read(rel)
    result, actual = re.subn(pattern, new, text, count=expected, flags=flags)
    if actual != expected:
        raise RuntimeError(f"{rel}: expected {expected} regex replacements, found {actual}: {pattern!r}")
    write(rel, result)


